-- 2. Create Tables (Run as HR user)
-- ======================================================

CREATE TABLE Course (
    CourseID NUMBER PRIMARY KEY,
    CourseName VARCHAR2(100) NOT NULL
);

CREATE TABLE Student (
    StudentID NUMBER PRIMARY KEY,
    Name VARCHAR2(100) NOT NULL,
    Email VARCHAR2(100) UNIQUE,
    STUDENTPASS VARCHAR2(100)
);

CREATE TABLE Instructor (
    InstructorID NUMBER PRIMARY KEY,
    Name VARCHAR2(100) NOT NULL,
    Email VARCHAR2(100) UNIQUE,
    INSTRUCTORPASS VARCHAR2(100)
);

CREATE TABLE Enrollment (
    EnrollmentID NUMBER PRIMARY KEY,
    StudentID NUMBER REFERENCES Student(StudentID),
    CourseID NUMBER REFERENCES Course(CourseID)
);

CREATE TABLE courseSession (
    SessionID NUMBER PRIMARY KEY,
    CourseID NUMBER REFERENCES Course(CourseID),
    SessionDate DATE,
    StartTime VARCHAR2(20),
    EndTime VARCHAR2(20)
);

CREATE TABLE Attendance (
    AttendanceID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    StudentID NUMBER REFERENCES Student(StudentID),
    SessionID NUMBER REFERENCES courseSession(SessionID),
    Status VARCHAR2(20) -- Present, Absent, Excused
);

CREATE TABLE Notification (
    NotificationID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    Title VARCHAR2(100),
    Description VARCHAR2(500),
    Type VARCHAR2(20), -- Success, Warning, Info
    RecipientID NUMBER, -- StudentID or NULL for all
    CreatedAt TIMESTAMP DEFAULT CURRENT_TIMESTAMP
);

